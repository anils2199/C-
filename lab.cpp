#include <iostream>
using namespace std;
int main () {
	int x, y;
	cout << "Enter 1st number:";
	cin >> x;
	cout << "Enter 2nd number:";
	cin >> y;
	cout << "Sum of integers: " << x + y << endl;
	cout << "Subtraction of integers: " << x - y << endl;
	cout << "Product of integers: " << x * y << endl;
	cout << "Average of integers: " << (x + y) / 2 << endl;
	cout << "Thank you!" << endl;
	return 0;
}
		
